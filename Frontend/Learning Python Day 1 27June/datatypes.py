#file to learn about datatypes in python
#integers
a=10
print(a)
print(type(a))  

#float
b=10.5
print(b)
print(type(b))

#complex
c=10+5j
print(c)
print(type(c))

#string
d="hello"
print(d)
print(type(d))

#boolean
e=True
print(e)
print(type(e))

#list
f=[10,20,30,40]
print(f)
print(type(f))

#tuple
g=(10,20,30,40)
print(g)
print(type(g))

#set
h={10,20,30,40}
print(h)
print(type(h))

#dictionary 
i={"name":"john","age":25}
print(i)
print(type(i))

#range
j=range(10)
print(j)
print(type(j))

#bytes
k=b"hello"
print(k)
print(type(k))

